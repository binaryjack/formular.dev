/**
 * Gets the day of the week as a number (0-6) for a given date string.
 *
 * @param dateStr - A string representing a date. The string should be in a format
 *                  that is recognized by the `Date` constructor.
 * @returns The day of the week as a number, where 0 represents Sunday, 1 represents Monday,
 *          and so on up to 6, which represents Saturday.
 */
export const getDayNumber = (dateStr: string) => new Date(dateStr).getDay()
