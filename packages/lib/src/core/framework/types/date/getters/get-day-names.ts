/**
 * Retrieves the abbreviated names of the days of the week.
 *
 * @returns {string[]} An array of strings representing the abbreviated day names,
 * starting with 'Sun' for Sunday and ending with 'Sat' for Saturday.
 */
export const getDayNames = (): string[] => {
    return ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat']
}
