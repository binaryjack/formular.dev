import { getMonthNames } from './get-month-names'

/**
 * Retrieves the name of a month based on its zero-based index.
 *
 * @param index - The zero-based index of the month (0 for January, 1 for February, etc.).
 * @returns The name of the month corresponding to the given index.
 *
 * @throws If the index is out of range or if `getMonthNames()` does not return a valid array.
 */
export const getMonthNameByIndex = (index: number): string => {
    return getMonthNames()[index]
}
